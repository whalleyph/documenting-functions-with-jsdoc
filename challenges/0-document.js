/**
 * Given a number, returns a negative version of the number.
 * 
 * @param {number} NUM - input number
 * 
 * @returns {number} if 'NUM' is positve: '-NUM' else: 'NUM'
 */
function asNegative(NUM) {
  if (NUM > 0) {
    return -NUM;
  } else {
    return NUM;
  }
}


